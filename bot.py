def generate_question_with_llm(action_description, hotkey):
    """Генерирует один вопрос через LLM"""
    prompt = f"""Напиши вопрос о горячей клавише Blender.

Действие: {action_description}
Клавиша: {hotkey}

ПРАВИЛА:
1. В вопросе НЕ ДОЛЖНО БЫТЬ самой клавиши {hotkey}
2. Вопрос должен быть на русском языке
3. Используй правильный падеж для действия
4. Напиши только вопрос, ничего больше

Вопрос:"""

    inputs = tokenizer(prompt, return_tensors="pt", max_length=500, truncation=True).to(model.device)

    with torch.no_grad():
        outputs = model.generate(
            **inputs,
            max_new_tokens=80,
            temperature=0.7,
            do_sample=True,
            top_p=0.9,
            pad_token_id=tokenizer.pad_token_id
        )

    full_response = tokenizer.decode(outputs[0], skip_special_tokens=True)
    question = full_response.replace(prompt, "").strip()
    question = question.split('\n')[0].strip()

    if "?" in question:
        question = question.split("?")[0] + "?"

    if hotkey in question:
        question = question.replace(hotkey, "").strip()

    if not question.endswith('?'):
        question += "?"

    return question

def get_next_question(topic, asked_keys):
    """
    Получает следующий вопрос, исключая повторения
    """
    if topic not in HOTKEYS_DB:
        return None, None, None

    all_keys = list(HOTKEYS_DB[topic].keys())
    available_keys = [k for k in all_keys if k not in asked_keys]

    if not available_keys:
        available_keys = all_keys
        asked_keys.clear()

    hotkey = random.choice(available_keys)
    action_description = HOTKEYS_DB[topic][hotkey]
    question = generate_question_with_llm(action_description, hotkey)

    return question, hotkey, action_description

