@dp.callback_query(lambda call: call.data.startswith("topic_"))
async def handle_topic(call: types.CallbackQuery):
    chat_id = call.message.chat.id
    topic = call.data.replace("topic_", "")

    if chat_id not in user_sessions:
        user_sessions[chat_id] = {
            "current_topic": None,
            "asked_keys": [],
            "current_hotkey": None,
            "current_question": None,
            "current_action": None,
            "statistics": {}
        }

    user_sessions[chat_id]["current_topic"] = topic
    user_sessions[chat_id]["asked_keys"] = []

    await call.answer(f"Выбрано: {topic}")

    try:
        await call.message.delete()
    except:
        pass

    loading_msg = await call.message.answer("🐾  Нейросеть генерирует вопрос, на это требуется время...")
    await ask_next_question(chat_id, loading_msg.message_id)

async def ask_next_question(chat_id: int, loading_msg_id: int = None):
    session = user_sessions.get(chat_id)
    if not session or not session["current_topic"]:
        await bot.send_message(chat_id, "Ошибка. Пожалуйста, начните заново с /start")
        return

    topic = session["current_topic"]
    asked_keys = session["asked_keys"]

    question, hotkey, action_description = await asyncio.to_thread(get_next_question, topic, asked_keys)

    session["asked_keys"].append(hotkey)
    session["current_question"] = question
    session["current_hotkey"] = hotkey
    session["current_action"] = action_description

    if loading_msg_id:
        try:
            await bot.delete_message(chat_id, loading_msg_id)
        except:
            pass

    await bot.send_message(
        chat_id,
        f"*{question}*\n\nฅ^•⩊•^ฅ Напиши свой ответ:",
        parse_mode=ParseMode.MARKDOWN
    )

@dp.message()
async def handle_answer(message: Message):
    chat_id = message.chat.id

    if message.text and message.text.startswith('/'):
        return

    if chat_id not in user_sessions or not user_sessions[chat_id]["current_topic"]:
        await message.answer("Пожалуйста, начните с команды /start")
        return

    session = user_sessions[chat_id]

    if not session["current_hotkey"]:
        await message.answer("Сначала выберите тему с помощью /start")
        return

    user_answer = message.text.strip().lower().replace(" ", "")
    expected_answer = session["current_hotkey"].lower().replace(" ", "")
    is_correct = user_answer == expected_answer

    current_topic = session["current_topic"]
    if current_topic not in session["statistics"]:
        session["statistics"][current_topic] = {"total": 0, "correct": 0}

    session["statistics"][current_topic]["total"] += 1
    if is_correct:
        session["statistics"][current_topic]["correct"] += 1

    if is_correct:
        result_text = (
            f"😻 *Правильно!* Отлично!\n\n"
            f"Вопрос: {session['current_question']}\n"
            f"Ваш ответ: `{message.text}`"
        )
    else:
        result_text = (
            f"🙀 *Неправильно*\n\n"
            f"Вопрос: {session['current_question']}\n"
            f"Ваш ответ: `{message.text}`\n"
            f"Правильный ответ: `{session['current_hotkey']}`\n\n"
            f"😿 Запомните эту комбинацию!"
        )

    await message.answer(result_text, parse_mode=ParseMode.MARKDOWN)
    await message.answer(
        "(ﾐ◕ᆽ<ﾐ)  *Что дальше?*",
        parse_mode=ParseMode.MARKDOWN,
        reply_markup=after_answer_keyboard()
    )

@dp.callback_query(lambda call: call.data in ["same_topic", "change_topic", "end_training"])
async def handle_after_answer(call: types.CallbackQuery):
    chat_id = call.message.chat.id
    action = call.data

    if action == "same_topic":
        await call.answer("Задаю новый вопрос...")
        await call.message.delete()
        loading_msg = await call.message.answer("🐾  Нейросеть генерирует новый вопрос, на это требуется время...")
        await ask_next_question(chat_id, loading_msg.message_id)

    elif action == "change_topic":
        await call.answer("Выберите новую тему")
        await call.message.edit_text(
            "*Выберите раздел:*",
            parse_mode=ParseMode.MARKDOWN,
            reply_markup=topics_keyboard()
        )

    elif action == "end_training":
        await call.answer("Тренировка завершена")

        session = user_sessions[chat_id]
        statistics = session.get("statistics", {})

        if statistics:
            stats_text = "😼 *Статистика правильных ответов:*\n\n"
            total_all = 0
            correct_all = 0

            for topic, stats in statistics.items():
                total = stats["total"]
                correct = stats["correct"]
                percentage = (correct / total * 100) if total > 0 else 0
                stats_text += f"*{topic}*\n"
                stats_text += f"   😽 {correct}/{total} ({percentage:.1f}%)\n\n"
                total_all += total
                correct_all += correct

            total_percentage = (correct_all / total_all * 100) if total_all > 0 else 0
            stats_text += f"*Итого:*\n"
            stats_text += f"   😻 {correct_all}/{total_all} ({total_percentage:.1f}%)\n"
        else:
            stats_text = "*Статистика:*\nНет отвеченных вопросов за эту сессию.\n"

        end_text = (
            "*Тренировка завершена!*\n\n"
            f"{stats_text}\n"
            "Чтобы начать новую тренировку, используй команду /start"
        )

        await call.message.edit_text(end_text, parse_mode=ParseMode.MARKDOWN)

        user_sessions[chat_id] = {
            "current_topic": None,
            "asked_keys": [],
            "current_hotkey": None,
            "current_question": None,
            "current_action": None,
            "statistics": {}
        }

